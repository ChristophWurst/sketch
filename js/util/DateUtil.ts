class DateUtil {

	public now() {
		var now = new Date();
		var year = now.getFullYear();
		var month = now.getMonth() + 1;
		var day = now.getDate();
		var hour = now.getHours();
		var minutes = now.getMinutes();

		return [year, month, day].join('-')
			+ ' ' + [hour, minutes].join(':');
	}

}

export = DateUtil;